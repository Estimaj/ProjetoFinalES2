public class ReplicaServidor {

    private String localizacaoReplicaServidor;

    public ReplicaServidor(String localizacaoReplicaServidor) {
        this.localizacaoReplicaServidor = localizacaoReplicaServidor;
    }

    public String getLocalizacaoReplicaServidor() {
        return localizacaoReplicaServidor;
    }

    public void setLocalizacaoReplicaServidor(String localizacaoReplicaServidor) {
        this.localizacaoReplicaServidor = localizacaoReplicaServidor;
    }
}
